function greet()
{
    return "Hello, Welcome";
}
console.log(greet());       //calling a function with no arguments

function welcome(name)
{
    console.log("Hello, Welcome ",name);
}
welcome("Kabil")            //calling a function with arguments.

function greetings(name)
{
    return `Hi ${name} welcome to rently`;  //usage of backtick to read more visibly.
}
console.log(greetings("Kabil"));

function add(a,b)
{
    return a*b;
}
console.log(add(4,5));   //function with a return type






